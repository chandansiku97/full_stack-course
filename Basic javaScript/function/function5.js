console.log(Math.floor(4.5)); // floor() method rounds a number DOWNWARDS to the nearest integer, and returns the result.
console.log(Math.ceil(4.5)); // ceil() method rounds a number UPWARDS to the nearest integer, and returns the result.
console.log(Math.round(4.4)); // round() method rounds a number to the nearest integer, and returns the result.
console.log(Math.min(1, 2, 3, 4, 5)); // min() method returns the number with the lowest value.
console.log(Math.max(1, 2, 3, 4, 5)); // max() method returns the number with the highest value.
console.log(Math.sqrt(98)); // sqrt() method returns the square root of a number.
console.log(Math.random()); // random() method returns a random number between 0 (inclusive), and 1 (exclusive).
